public class FormInvestissement {
    private double montantInvestissement;
    private String nomBanque;
    private String numeroTransit;
    private String numeroInstitution;
    private String numeroCompte;

    // Constructeur
    public FormInvestissement(double montantInvestissement, String nomBanque, String numeroTransit, String numeroInstitution, String numeroCompte) {
        this.montantInvestissement = montantInvestissement;
        this.nomBanque = nomBanque;
        this.numeroTransit = numeroTransit;
        this.numeroInstitution = numeroInstitution;
        this.numeroCompte = numeroCompte;
    }


}



